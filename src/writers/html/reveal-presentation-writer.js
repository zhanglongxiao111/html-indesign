const { HTML_DATA_ID_ATTRIBUTES } = require('../../protocol');
const fs = require('fs');
const path = require('path');
const { readAuthorPackage } = require('../../authoring');

function writeRevealPresentation(configPath, options = {}) {
  const sourcePackage = readAuthorPackage(configPath);
  copyRevealAssets(sourcePackage.rootDir);

  const styles = sourcePackage.styleFiles
    .map((file) => {
      const css = fs.readFileSync(file.filePath, 'utf8');
      return `<style data-source-file="${attr(file.relativePath)}">\n${css.trimEnd()}\n</style>`;
    })
    .join('\n');
  const pages = sourcePackage.pageFiles
    .map((file) => addSourceFileAttribute(fs.readFileSync(file.filePath, 'utf8'), file.relativePath))
    .join('\n\n');
  const title = sourcePackage.config.title || sourcePackage.config.id;
  const width = positiveNumber(options.width) || positiveNumber(sourcePackage.config.presentation && sourcePackage.config.presentation.width) || 1600;
  const height = positiveNumber(options.height) || positiveNumber(sourcePackage.config.presentation && sourcePackage.config.presentation.height) || 900;
  const presentationPath = path.join(sourcePackage.rootDir, 'presentation.html');

  const html = [
    '<!doctype html>',
    '<html lang="zh-CN">',
    '<head>',
    '  <meta charset="utf-8">',
    `  <title>${escapeText(title)} - 演示预览</title>`,
    '  <!-- Generated by html-indesign. Use deck.html for HTML-to-InDesign conversion; this file is reveal.js preview only. -->',
    '  <link rel="stylesheet" href="vendor/reveal/reset.css">',
    '  <link rel="stylesheet" href="vendor/reveal/reveal.css">',
    styles,
    presentationChromeCss(width, height),
    '</head>',
    '<body>',
    `  <div class="reveal hi-reveal" ${HTML_DATA_ID_ATTRIBUTES.IGNORE}="true">`,
    '    <div class="slides">',
    indent(pages, 6),
    '    </div>',
    '  </div>',
    '  <script src="vendor/reveal/reveal.js"></script>',
    '  <script>',
    '    Reveal.initialize({',
    `      width: ${formatNumber(width)},`,
    `      height: ${formatNumber(height)},`,
    '      hash: true,',
    '      controls: true,',
    '      progress: true,',
    '      slideNumber: true,',
    '      center: false,',
    '      transition: "none",',
    '      backgroundTransition: "none",',
    '      margin: 0.02',
    '    });',
    '  </script>',
    '</body>',
    '</html>',
    '',
  ].join('\n');

  fs.writeFileSync(presentationPath, html, 'utf8');
  return { path: presentationPath };
}

function copyRevealAssets(rootDir) {
  const distDir = path.dirname(require.resolve('reveal.js'));
  const targetDir = path.join(rootDir, 'vendor', 'reveal');
  fs.mkdirSync(targetDir, { recursive: true });
  for (const file of ['reset.css', 'reveal.css', 'reveal.js']) {
    fs.copyFileSync(path.join(distDir, file), path.join(targetDir, file));
  }
}

function presentationChromeCss(width, height) {
  return [
    '<style data-source-file="presentation-preview">',
    'html, body { width: 100%; height: 100%; margin: 0; background: #1f2326; }',
    '.hi-reveal { background: #1f2326; }',
    '.hi-reveal .slides { text-align: initial; }',
    `.hi-reveal .slides > section.page { width: ${formatNumber(width)}px; height: ${formatNumber(height)}px; padding: 0; }`,
    '.hi-reveal .slides > section.page, .hi-reveal .slides > section.page.present { display: grid; }',
    '.hi-reveal .controls, .hi-reveal .progress, .hi-reveal .slide-number { color: #ffffff; }',
    '</style>',
  ].join('\n');
}

function addSourceFileAttribute(fragment, sourceFile) {
  return fragment.replace(/<section\b([^>]*)>/i, (match, rest) => {
    if (new RegExp(`${escapeRegExp(HTML_DATA_ID_ATTRIBUTES.SOURCE_FILE)}\\s*=`).test(rest)) return match;
    return `<section${rest} ${HTML_DATA_ID_ATTRIBUTES.SOURCE_FILE}="${attr(sourceFile)}">`;
  }).trimEnd();
}

function escapeRegExp(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function positiveNumber(value) {
  const number = Number(value);
  return Number.isFinite(number) && number > 0 ? number : null;
}

function formatNumber(value) {
  return String(Math.round(Number(value) * 1000) / 1000);
}

function indent(value, spaces) {
  const prefix = ' '.repeat(spaces);
  return String(value || '').split(/\r?\n/).map((line) => (line ? `${prefix}${line}` : line)).join('\n');
}

function attr(value) {
  return String(value).replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;');
}

function escapeText(value) {
  return String(value).replace(/&/g, '&amp;').replace(/</g, '&lt;');
}

module.exports = {
  writeRevealPresentation,
};
